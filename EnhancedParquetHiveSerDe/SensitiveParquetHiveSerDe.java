public class SensitiveParquetHiveSerDe extends ParquetHiveSerDe {


  @Override
  public void initialize(final Configuration conf, final Properties tbl) throws SerDeException {
    super.initialize(conf, twist(tbl));
  }

  private Properties twist(Properties tbl){
    Properties ret = new Properties();
    ret.setProperty("columns.types", tbl.getProperty("columns.types"));
    ret.setProperty("columns", tbl.getProperty("casesensitive"));
    return ret;
  }
}

